#!/usr/bin/env node
/**
 * Import official logo PNG and generate all web assets.
 *
 * Usage:
 *   npm run import-logo -- path/to/official-logo.png
 *
 * Generates optimized PNGs and updates src/lib/logo-config.ts to use them.
 */
import { mkdir, readFile, writeFile } from "node:fs/promises";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";
import sharp from "sharp";
import pngToIco from "png-to-ico";

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = join(__dirname, "..");
const officialDir = join(root, "public/logo/official");
const PNG_SIZES = [16, 32, 48, 72, 96, 128, 144, 152, 180, 192, 256, 384, 512];

const inputPath = process.argv[2];
if (!inputPath) {
  console.error("Usage: npm run import-logo -- <path-to-logo.png>");
  process.exit(1);
}

async function writeConfig(meta) {
  const config = `/**
 * Logo asset paths — auto-generated by scripts/import-official-logo.mjs
 * Do not edit manually. Re-run import-logo to update.
 */
export const LOGO_ASSETS = {
  useOfficial: true,
  header: "/logo/official/logo-header.png",
  footer: "/logo/official/logo-footer.png",
  mark: "/logo/official/logo-mark.png",
  markDark: "/logo/official/logo-mark.png",
  headerWidth: ${meta.headerWidth},
  headerHeight: ${meta.headerHeight},
  footerWidth: ${meta.footerWidth},
  footerHeight: ${meta.footerHeight},
  markSize: 48,
} as const;
`;
  await writeFile(join(root, "src/lib/logo-config.ts"), config);
  console.log("Updated src/lib/logo-config.ts");
}

async function generateIcons(markPath) {
  const iconsDir = join(root, "public/icons");
  const appDir = join(root, "src/app");
  const mark = await readFile(markPath);

  for (const size of PNG_SIZES) {
    await sharp(mark).resize(size, size).png().toFile(join(iconsDir, `icon-${size}.png`));
  }

  await sharp(mark).resize(180, 180).png().toFile(join(appDir, "apple-icon.png"));

  const icoBuffers = await Promise.all(
    [16, 32, 48].map((size) => sharp(mark).resize(size, size).png().toBuffer())
  );
  const ico = await pngToIco(icoBuffers);
  await writeFile(join(appDir, "favicon.ico"), ico);
  await writeFile(join(root, "public/favicon.ico"), ico);

  const maskable = await sharp(mark)
    .resize(308, 308)
    .extend({ top: 102, bottom: 102, left: 102, right: 102, background: { r: 14, g: 20, b: 28, alpha: 1 } })
    .png()
    .toBuffer();
  await writeFile(join(iconsDir, "icon-maskable-512.png"), maskable);
  console.log("Generated favicon and PWA icons from official mark");
}

async function main() {
  await mkdir(officialDir, { recursive: true });
  const input = await readFile(inputPath);
  const meta = await sharp(input).metadata();
  const w = meta.width ?? 800;
  const h = meta.height ?? 300;

  // Detect layout: if image is roughly square, treat as footer (with tagline)
  const hasTagline = h / w > 0.25;
  const headerCropH = hasTagline ? Math.round(h * 0.55) : h;

  const headerBuf = await sharp(input)
    .extract({ left: 0, top: 0, width: w, height: headerCropH })
    .png()
    .toBuffer();
  await sharp(headerBuf).resize(640, null, { fit: "inside" }).png().toFile(join(officialDir, "logo-header.png"));

  const headerMeta = await sharp(headerBuf).metadata();
  await sharp(input).resize(640, null, { fit: "inside" }).png().toFile(join(officialDir, "logo-footer.png"));

  const footerMeta = await sharp(join(officialDir, "logo-footer.png")).metadata();

  const markW = Math.round(w * 0.17);
  await sharp(input)
    .extract({ left: 0, top: 0, width: markW, height: headerCropH })
    .resize(512, 512, { fit: "contain", background: { r: 14, g: 20, b: 28, alpha: 1 } })
    .png()
    .toFile(join(officialDir, "logo-mark.png"));

  console.log(`Generated ${officialDir}/logo-header.png`);
  console.log(`Generated ${officialDir}/logo-footer.png`);
  console.log(`Generated ${officialDir}/logo-mark.png`);

  await writeConfig({
    headerWidth: headerMeta.width ?? 640,
    headerHeight: headerMeta.height ?? 120,
    footerWidth: footerMeta.width ?? 640,
    footerHeight: footerMeta.height ?? 200,
  });

  await generateIcons(join(officialDir, "logo-mark.png"));
  console.log("\nDone! Official logo imported. Run npm run build to deploy.");
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
